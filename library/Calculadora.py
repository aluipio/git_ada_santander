class Calculadora:

    def __init__(self):
        pass

    # Recebe dois números e retorna a soma
    def soma(self, x, y):
        return x+y

    # Recebe dois números e retorna o produto
    def multiplicacao(self, x, y):
        return x*y

    # Recebe dois números e retorna a divisão do primeiro pelo segundo
    def divisao(self, x, y):
        return x/y
    
    # Recebe dois números e retorna a subtração
    def subtracao(self, x, y):
        return x-y    
    
    # Recebe dois números e retorna a potência
    def potencia(self, x, y):
        return x**y    